//! `FerroTunnel` Examples
//!
//! This crate provides examples demonstrating how to embed `FerroTunnel`
//! server and client in your own Rust applications.
//!
//! ## Available Examples
//!
//! - `embedded_server` - Shows how to embed a `FerroTunnel` server
//! - `embedded_client` - Shows how to embed a `FerroTunnel` client
//!
//! ## Running Examples
//!
//! ```bash
//! # Start the embedded server
//! cargo run -p ferrotunnel-examples --example embedded_server
//!
//! # In another terminal, start the embedded client
//! cargo run -p ferrotunnel-examples --example embedded_client
//! ```
